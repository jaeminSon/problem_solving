def solution(s):
    asciicodes = [' ', 'a','b','c','d','e','f','g','h','i','j','k','l','m','n','o','p','q','r','s','t','u','v','w','x','y','z']
    braillecodes = ['000000',
                    '100000',
                    '110000',
                    '100100',
                    '100110',
                    '100010',
                    '110100',
                    '110110',
                    '110010',
                    '010100',
                    '010110',
                    '101000',
                    '111000',
                    '101100',
                    '101110',
                    '101010',
                    '111100',
                    '111110',
                    '111010',
                    '011100',
                    '011110',
                    '101001',
                    '111001',
                    '010111',
                    '101101',
                    '101111',
                    '101011']
    
        
    dict_braille = {asciicodes[i]:braillecodes[i] for i in range(len(asciicodes))}
    
    res = ""
    for c in s:
        if c.isupper():
            res = res + '000001'
        res += dict_braille[c.lower()]
        
    return res
        
