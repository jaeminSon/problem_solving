from heapq import heappop, heappush

def solution(map):
    
    h = len(map)
    w = len(map[0])

    def dikstra():
        visited = set()
        q = [(1,0,0)]
        while q:
            el = heappop(q)
            dist, y, x = el
            if y==h-1 and x==w-1:
                return dist
            elif (y,x) not in visited:
                visited.add((y,x))
                for dy, dx in [(-1, 0), (1,0), (0,-1), (0,1)]:
                    yval = y+dy
                    xval = x+dx
                    if 0 <= yval and yval < h and 0 <= xval and xval < w and map[yval][xval]==0:
                        heappush(q, (dist+1, yval, xval))
        return h * w + 1

    min_val = dikstra()
    for i in range(h):
        for j in range(w):
            if map[i][j] == 1:
                map[i][j] = 0
                min_val = min(min_val, dikstra())
                map[i][j] = 1

    return min_val




